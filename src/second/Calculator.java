package second;

public class Calculator {

    public double sum(double numb, double numb2) {
        return numb + numb2;
    }

    public double minus(double numb, double numb2) {
        return numb - numb2;
    }

    public double devision(double numb, double numb2) {
        return numb / numb2;
    }

    public double multiplikation(double numb, double numb2) {
        return numb * numb2;
    }

    public double pow(double numb, int degree) {
        return Math.pow(numb, degree);
    }
}
